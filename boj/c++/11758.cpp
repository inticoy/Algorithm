#include <iostream>

int main() {
  int x1, x2, x3, y1, y2, y3 = 0;
  std::cin >> x1 >> y1 >> x2 >> y2 >> x3 >> y3;

  int a1, a2, b1, b2 = 0;
  a1 = x2 - x1;
  b1 = y2 - y1;
  a2 = x3 - x2;
  b2 = y3 - y2;

  int outer;
  outer = a1 * b2 - a2 * b1;
  if (outer == 0) {
	  std::cout << "0\n";
  } else if (outer < 0) {
	  std::cout << "-1\n";
  } else {
	  std::cout << "1\n";
  }
  return 0;
}
